        public static Models.NameProfile GetName()
        {
            var file = $"config/luke_mode_names.txt";
            var nameArray = file.GetRandomFromFile().Split(Convert.ToChar(","));
            var name = new Models.NameProfile
            {
                First = nameArray[0],
                Last = nameArray[1]
            };
            return name;
        }

